function verificarDiaDaSemana(dia) {
    switch(dia) {
        case 1:
            console.log('domingo'); break;
        case 2:
            console.log('segunda-feira'); break;
        case 3:
            console.log('terça-feira'); break;
        case 4:
            console.log('quarta-feira'); break;
        case 5:
            console.log('quinta-feira'); break;
        case 6:
            console.log('sexta-feira'); break;
        case 7:
            console.log('sábado'); break;
        default:
            console.log('dia inválido');
    }
}

verificarDiaDaSemana(1)
verificarDiaDaSemana(2)
verificarDiaDaSemana(3)
verificarDiaDaSemana(4)
verificarDiaDaSemana(5)
